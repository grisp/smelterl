        #{
            main => MainTarget,
            aux_alpha => AuxAlphaTarget,
            aux_beta => AuxBetaTarget
        },
        [aux_beta, aux_alpha],
        #{
            product => demo,
            target_arch => <<"arm-buildroot-linux-gnueabihf">>,
            product_fields => #{},
            repositories => [],
            nugget_repo_map => #{demo => undefined},
            nuggets => [],
            auxiliary_products => [],
            capabilities => #{},
            sdk_outputs => [],
            external_components => [],
            smelterl_repository => smelterl
        }
    ),
