-module(smelterl_plan_SUITE).

-include_lib("common_test/include/ct.hrl").

-export([all/0]).
-export([
    write_build_plan_env_with_loop_metadata/1,
    write_and_read_roundtrip_build_plan/1,
    read_rejects_unsupported_plan_version/1
]).

all() ->
    [
        write_build_plan_env_with_loop_metadata,
        write_and_read_roundtrip_build_plan,
        read_rejects_unsupported_plan_version
    ].

write_build_plan_env_with_loop_metadata(_Config) ->
    OutputDir = make_temp_dir("smelterl-plan-env"),
    OutputPath = filename:join(OutputDir, "build_plan.env"),
    BuildPlan = sample_plan_with_auxiliaries(),
    ok = smelterl_plan:write_env_file(OutputPath, BuildPlan),
    {ok, FileContents} = file:read_file(OutputPath),
    assert_contains(FileContents, <<"# Generated by smelterl - do not edit">>),
    assert_contains(FileContents, <<"ALLOY_PLAN_PRODUCT='demo'">>),
    assert_contains(FileContents, <<"ALLOY_PLAN_AUXILIARY_IDS=('aux_beta' 'aux_alpha')">>),
    assert_contains(
        FileContents,
        <<"ALLOY_PLAN_TARGET_IDS=('aux_beta' 'aux_alpha' 'main')">>
    ),
    assert_order(
        FileContents,
        <<"[\'aux_beta\']='auxiliary'">>,
        <<"[\'main\']='main'">>
    ),
    assert_order(
        FileContents,
        <<"[\'ALLOY_MOTHERLODE\']='${ALLOY_MOTHERLODE}'">>,
        <<"[\'FOO\']='bar baz'">>
    ),
    assert_equal(
        <<
            "product=demo\n"
            "main=main\n"
            "aux=aux_beta aux_alpha\n"
            "targets=aux_beta aux_alpha main\n"
            "kind=auxiliary\n"
            "root=aux_beta_root\n"
            "extra=${ALLOY_MOTHERLODE}\n"
        >>,
        source_env_and_print(OutputPath)
    ).

write_and_read_roundtrip_build_plan(_Config) ->
    OutputDir = make_temp_dir("smelterl-plan-roundtrip"),
    OutputPath = filename:join(OutputDir, "build_plan.term"),
    BuildPlan = sample_plan(),
    ok = smelterl_plan:write_file(OutputPath, BuildPlan),
    {ok, FileContents} = file:read_file(OutputPath),
    assert_contains(FileContents, <<"%% coding: utf-8">>),
    assert_contains(FileContents, <<"{build_plan,<<\"1.0\">>">>),
    {ok, ReadPlan} = smelterl_plan:read_file(OutputPath),
    assert_equal(BuildPlan, ReadPlan),
    {ok, MainTarget} = smelterl_plan:select_target(main, ReadPlan),
    assert_equal(main, maps:get(kind, MainTarget)).

read_rejects_unsupported_plan_version(_Config) ->
    OutputDir = make_temp_dir("smelterl-plan-unsupported"),
    OutputPath = filename:join(OutputDir, "build_plan.term"),
    ok = file:write_file(
        OutputPath,
        unicode:characters_to_binary(
            smelterl_file:format_term({build_plan, <<"9.9">>, []})
        )
    ),
    assert_equal(
        {error, {unsupported_plan_version, <<"9.9">>}},
        smelterl_plan:read_file(OutputPath)
    ).

sample_plan() ->
    sample_plan_without_auxiliaries().

