sample_capabilities(AuxiliaryIds) ->
    #{
        firmware_variants => [plain],
        variant_nuggets => #{plain => []},
        selectable_outputs => [],
        firmware_parameters => [],
        sdk_outputs_by_target =>
            maps:from_list([{TargetId, []} || TargetId <- [main | AuxiliaryIds]])
    }.

make_temp_dir(Prefix) ->
    make_temp_dir(Prefix, 0).

make_temp_dir(Prefix, Attempt) ->
    Suffix =
        integer_to_list(erlang:system_time(nanosecond)) ++ "-" ++
        integer_to_list(erlang:unique_integer([monotonic, positive])) ++ "-" ++
        integer_to_list(Attempt),
    Base = filename:join(os:getenv("TMPDIR", "/tmp"), Prefix ++ "-" ++ Suffix),
    case file:make_dir(Base) of
        ok ->
            Base;
        {error, eexist} ->
            make_temp_dir(Prefix, Attempt + 1)
    end.

assert_contains(Haystack, Needle) ->
    case binary:match(Haystack, Needle) of
        nomatch ->
            ct:fail("Expected ~tp to contain ~tp", [Haystack, Needle]);
        _ ->
            ok
    end.

assert_order(Haystack, FirstNeedle, SecondNeedle) ->
    case {
        binary:match(Haystack, FirstNeedle),
        binary:match(Haystack, SecondNeedle)
    } of
        {{FirstIndex, _}, {SecondIndex, _}} when FirstIndex < SecondIndex ->
            ok;
        {FirstMatch, SecondMatch} ->
            ct:fail(
                "Expected ~tp before ~tp in ~tp, got ~tp and ~tp",
                [FirstNeedle, SecondNeedle, Haystack, FirstMatch, SecondMatch]
            )
    end.

assert_equal(Expected, Actual) ->
    case Actual of
        Expected ->
            ok;
        _ ->
            ct:fail("Expected ~tp, got ~tp", [Expected, Actual])
    end.

source_env_and_print(Path) ->
    Port =
        open_port(
            {spawn_executable, os:find_executable("bash")},
            [
                binary,
                exit_status,
                stderr_to_stdout,
                use_stdio,
                {
                    args,
                    [
                        "-lc",
                        lists:flatten([
                            "source \"$1\"\n",
                            "printf 'product=%s\\n' \"$ALLOY_PLAN_PRODUCT\"\n",
                            "printf 'main=%s\\n' \"$ALLOY_PLAN_MAIN_TARGET\"\n",
                            "printf 'aux=%s\\n' \"${ALLOY_PLAN_AUXILIARY_IDS[*]}\"\n",
                            "printf 'targets=%s\\n' \"${ALLOY_PLAN_TARGET_IDS[*]}\"\n",
                            "printf 'kind=%s\\n' \"${ALLOY_PLAN_TARGET_KIND[aux_beta]}\"\n",
                            "printf 'root=%s\\n' \"${ALLOY_PLAN_TARGET_ROOT[aux_beta]}\"\n",
                            "printf 'extra=%s\\n' \"${ALLOY_PLAN_EXTRA_CONFIG[ALLOY_MOTHERLODE]}\"\n"
                        ]),
                        "--",
                        Path
                    ]
                }
            ]
        ),
    collect_port_output(Port, []).

collect_port_output(Port, Acc) ->
    receive
        {Port, {data, Data}} ->
            collect_port_output(Port, [Data | Acc]);
        {Port, {exit_status, 0}} ->
            iolist_to_binary(lists:reverse(Acc));
        {Port, {exit_status, Status}} ->
            ct:fail("bash sourcing failed with status ~B and output ~tp", [
                Status,
                iolist_to_binary(lists:reverse(Acc))
            ])
    end.
