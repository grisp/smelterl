    Target = #{
        id => main,
        kind => main,
        tree => #{root => demo, edges => #{demo => []}},
        topology => [demo],
        motherlode => #{nuggets => #{}, repositories => #{}},
        config => #{
            <<"ALLOY_CONFIG_TARGET_ARCH_TRIPLET">> =>
                {global, undefined, <<"arm-buildroot-linux-gnueabihf">>}
        },
        defconfig => #{regular => [], cumulative => []},
        capabilities => #{
            firmware_variants => [plain],
            variant_nuggets => #{plain => []},
            selectable_outputs => [],
            firmware_parameters => [],
            sdk_outputs_by_target => #{main => []}
        }
    },
    {ok, Plan} = smelterl_plan:new(
        demo,
        #{<<"ALLOY_MOTHERLODE">> => <<"${ALLOY_MOTHERLODE}">>},
        #{main => Target},
        [],
        #{
            product => demo,
            target_arch => <<"arm-buildroot-linux-gnueabihf">>,
            product_fields => #{},
            repositories => [],
            nugget_repo_map => #{demo => undefined},
            nuggets => [],
            auxiliary_products => [],
            capabilities => #{},
            sdk_outputs => [],
            external_components => [],
            smelterl_repository => smelterl
        }
    ),
    Plan.

sample_plan_with_auxiliaries() ->
    MainTarget = #{
        id => main,
        kind => main,
        tree => #{root => demo, edges => #{demo => [platform_core], platform_core => []}},
        topology => [platform_core, demo],
        motherlode => #{nuggets => #{}, repositories => #{}},
        config => #{
            <<"ALLOY_CONFIG_TARGET_ARCH_TRIPLET">> =>
                {global, undefined, <<"arm-buildroot-linux-gnueabihf">>}
        },
        defconfig => #{regular => [], cumulative => []},
        capabilities => sample_capabilities([aux_alpha, aux_beta])
    },
    AuxAlphaTarget = #{
        id => aux_alpha,
        kind => auxiliary,
        aux_root => aux_alpha_root,
        constraints => [],
        tree => #{
            root => aux_alpha_root,
            edges => #{aux_alpha_root => [platform_core], platform_core => []}
        },
        topology => [platform_core, aux_alpha_root],
        motherlode => #{nuggets => #{}, repositories => #{}},
        config => #{},
        defconfig => #{regular => [], cumulative => []},
        capabilities => sample_capabilities([aux_alpha, aux_beta])
    },
    AuxBetaTarget = #{
        id => aux_beta,
        kind => auxiliary,
        aux_root => aux_beta_root,
        constraints => [],
        tree => #{
            root => aux_beta_root,
            edges => #{aux_beta_root => [platform_core], platform_core => []}
        },
        topology => [platform_core, aux_beta_root],
        motherlode => #{nuggets => #{}, repositories => #{}},
        config => #{},
        defconfig => #{regular => [], cumulative => []},
        capabilities => sample_capabilities([aux_alpha, aux_beta])
    },
    {ok, Plan} = smelterl_plan:new(
        demo,
        #{
            <<"FOO">> => <<"bar baz">>,
            <<"ALLOY_MOTHERLODE">> => <<"${ALLOY_MOTHERLODE}">>
